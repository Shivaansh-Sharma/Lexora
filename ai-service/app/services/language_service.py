def detect_language(text: str):

    result = get_language_detector()(text)

    prediction = result[0]

    code = prediction["label"]

    return {
        "language_code": code,

        "language":
            LANGUAGE_MAP.get(
                code,
                code.upper()
            ),

        "confidence":
            round(
                prediction["score"],
                4
            )
    }